# Task 2
# Написати скрипт, який буде повертати результат
# арифметичних дій +, -, *, /, // для заданих x та y.

number_x = int(input("x="))
number_y = int(input("y="))
print(f"{number_x} + {number_y} = {number_x+number_y}")
print(f"{number_x} - {number_y} = {number_x-number_y}")
print(f"{number_x} * {number_y} = {number_x*number_y}")
print(f"{number_x} / {number_y} = {number_x/number_y}")
print(f"{number_x} // {number_y} = {number_x//number_y}")